//ยกกำลัง
console.log('10^3 = ', Math.pow(10, 3));  
//ค่าน้อยสุด
console.log('min 1, 2, 3 = ', Math.min(1, 2, 3));
//ปัดปกติ
console.log('round = ', Math.round(10.6));
//ปัดลง
console.log('floor = ', Math.floor(10.9));
//ปัดขึ้น
console.log('ceil = ', Math.ceil(10.2));

console.log('random = ', Math.random());
console.log('random  1-10 = ', Math.round(Math.random()* 10));
console.log('PI = ', Math.PI);

console.log('max 10, 9, 5 = ', Math.max(10, 9, 5));